import fs from 'fs';
import path from 'path';

const SKIP = new Set(['.git', '.github', 'dashboard', 'scripts', 'node_modules']);
const EXTS = new Set(['.png', '.webp', '.avif', '.svg', '.ico']);

export const getFiles = (dir, baseCwd = dir) => {
  let results = [];
  let list;
  try { list = fs.readdirSync(dir); } catch { return results; }
  for (const file of list) {
    const full = path.join(dir, file);
    try {
      const stat = fs.statSync(full);
      if (stat.isDirectory()) {
        if (SKIP.has(file)) continue;
        results = results.concat(getFiles(full, baseCwd));
      } else {
        const ext = path.extname(file).toLowerCase();
        if (!EXTS.has(ext)) continue;
        const rel = path.relative(baseCwd, full);
        const parts = rel.split('/');
        results.push({
          name: path.basename(file),
          path: rel,
          project: /* v8 ignore next */ parts[0] || 'root',
          category: parts.length >= 3 ? parts[2] : 'general',
          format: ext.slice(1),
          size: stat.size
        });
      }
    } catch { /* skip unreadable */ }
  }
  return results;
};

export function generateManifest(cwd) {
  const manifest = getFiles(cwd, cwd);
  manifest.sort((a, b) => a.path.localeCompare(b.path));
  const outPath = path.join(cwd, 'manifest.json');
  fs.writeFileSync(outPath, JSON.stringify(manifest, null, 2));
  console.log(`Manifest generated: ${manifest.length} assets`);

  // Generate TypeScript definitions (additive — failures don't break manifest)
  try {
    generateTypeDefs(cwd, manifest);
  } catch (err) {
    console.warn(`TypeScript definitions skipped: ${err.message}`);
  }

  return manifest;
}

function generateTypeDefs(cwd, manifest) {
  if (!manifest.length) return;

  const paths = manifest.map((e) => e.path);
  const projects = [...new Set(manifest.map((e) => e.project))].sort();
  const formats = [...new Set(manifest.map((e) => e.format))].sort();

  const lines = [
    '/**',
    ' * Auto-generated by CloudCDN manifest pipeline.',
    ' * DO NOT EDIT — regenerated on every build.',
    ' *',
    " * Install: npm i cloudcdn-paths",
    " * Usage: import type { CdnPath } from 'cloudcdn-paths';",
    ' */',
    '',
    '/** Every valid asset path on CloudCDN */',
    'export type CdnPath =',
    ...paths.map((p, i) => `  | "${p}"${i === paths.length - 1 ? ';' : ''}`),
    '',
    '/** All project namespaces */',
    `export type CdnProject = ${projects.map((p) => `"${p}"`).join(' | ')};`,
    '',
    '/** All available formats */',
    `export type CdnFormat = ${formats.map((f) => `"${f}"`).join(' | ')};`,
    '',
    '/** Build a fully qualified CDN URL from a path */',
    'export declare function cdnUrl(path: CdnPath): string;',
    '',
  ];

  const dtsPath = path.join(cwd, 'cloudcdn-paths.d.ts');
  fs.writeFileSync(dtsPath, lines.join('\n'));
  console.log(`TypeScript definitions generated: ${paths.length} paths in cloudcdn-paths.d.ts`);
}

/* v8 ignore next 4 */
const isMain = import.meta.url === `file://${process.argv[1]}` || process.argv[1]?.endsWith('generate-manifest.mjs');
if (isMain) {
  generateManifest(process.cwd());
}
